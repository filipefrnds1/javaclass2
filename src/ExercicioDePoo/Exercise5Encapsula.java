//Em um banco, para se cadastrar uma conta bancária, é necessário informar o número da conta, o nome do titular da conta,
//e o valor de depósito inicial que o titular depositou ao abrir a conta. Este valor de depósito inicial, entretanto, é opcional,
//ou seja: se o titular não tiver dinheiro a depositar no momento de abrir sua conta, o depósito inicial não será feito e o saldo
//inicial da conta será, naturalmente, zero.

//Importante: uma vez que uma conta banária foi aberta, o número da conta nunca poderá ser alterado. Já o nome do titular pode ser
//alterado (pois uma pessoa pode mudar de nome por ocasião de casamento, por exemplo).

//Por fim, o saldo da conta não pode ser alterado livremente. É preciso haver um mecanismo para proteger isso. O saldo só aumenta
//por meio de depósitos, e só diminui por meio de saques. Para cada saque realizado, o banco cobra uma taca de $5.00. Nota: a conta 
//pode ficar com saldo negativo se o saldo não for suficiente para realizar o saque e/ou pagar a taxa.

//Você deve fazer um programa que realize o cadastro de uma conta, dando opção para que seja ou não informado o valor de depósito 
//inicial. Em seguida, realizar um depósito e depois um saque, sempre mostrando os dados da conta após cada operação.


package ExercicioDePoo;

import java.util.Locale;
import java.util.Scanner;

import entities.Banco;

public class Exercise5Encapsula {

	public static void main(String[] args) {
		Locale.setDefault(Locale.US);
		Scanner sc = new Scanner(System.in);
		
		int numeroConta;
		String nome;
		double valorConta;
		char simounao;
		Banco banco;
		
		double deposito;
		double saque;
		
		System.out.println("Enter account number: ");
		numeroConta = sc.nextInt();
		
		System.out.println("Enter account holder: ");
		sc.nextLine();
		nome = sc.nextLine();
		
		System.out.println("Is there an initial deposit (y/n) ? ");
		simounao = sc.next().charAt(0);	
		//System.out.println(simounao);	
		
		if(simounao == 'y') {
			System.out.println("Informe o valor de depósito inicial: ");
			valorConta = sc.nextDouble();
			banco = new Banco(numeroConta,nome,valorConta);
		}else {
			banco = new Banco(numeroConta,nome);
		}
		
		System.out.println("Account data: ");
		System.out.println(banco);
		
		System.out.println("Enter a deposit value: ");
		deposito = sc.nextDouble();
		banco.deposito(deposito);
		System.out.println("Update account data: ");
		System.out.println(banco);
		
		System.out.println("Enter a withdraw value: ");
		saque = sc.nextDouble();
		banco.saque(saque);
		System.out.println("Update account data: ");
		System.out.println(banco);
		
		
		
		
		
		
		sc.close();
	}

}
